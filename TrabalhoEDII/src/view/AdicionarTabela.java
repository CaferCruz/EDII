    private void jButtonSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSalvarActionPerformed
        if (jTextNomeTabela.getText() != null) {
            JOptionPane pop = new JOptionPane();
            //Mensagem de aalerta que n√£o exite nome da Tabela.
        } else {
             try {
                Catalago.adicionar(jTextNomeTabela.getText().concat(".dat"), campos);
            } catch (IOException ex) {
                Logger.getLogger(AdicionarTabela.class.getName()).log(Level.SEVERE, null, ex);
            }
            try {
                Tabela tab = new Tabela(jTextNomeTabela.getText().concat(".dat"), campos);
            } catch (IOException ex) {
                Logger.getLogger(AdicionarTabela.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jButtonSalvarActionPerformed

